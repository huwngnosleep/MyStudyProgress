# capitalize()	Converts the first character to upper case
txt = "hello, and welcome to my world."
print (txt.capitalize())

# casefold()	Converts string into lower case 
txt = "Hello, And Welcome To My World!"
print(txt.casefold())

# count()	Returns the number of times a specified value occurs in a string
txt = "I love apples, apple are my favorite fruit"
print(txt.count("apple"))

# find()	Searches a specified value and returns the position of where it was found
txt = "Hello, welcome to my world."
print(txt.find("welcome")) 

# index()	Searches a specified value and returns the position of where it was found
txt = "Hello, welcome to my world."
print(txt.index("welcome")) 